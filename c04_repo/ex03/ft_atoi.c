int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	sonuc;

	i = 0;
	sign = 1;
	sonuc = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sonuc = sonuc * 10 + (str[i] - '0');
		i++;
	}
	return (sonuc * sign);
}
