int	ft_len(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] >= 32 && str[i] <= 126 && str[i] != '\0')
	{
		i++;
	}
	if (i == ft_len(str))
		return (1);
	else
		return (0);
}
